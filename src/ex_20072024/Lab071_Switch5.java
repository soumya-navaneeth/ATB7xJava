package ex_20072024;

public class Lab071_Switch5 {
//    When Case value is negative
    public static void main(String[] args) {
        int a = 11;
        switch (-1){
            default:
                System.out.println("Default");
                break;
            case -1:
                System.out.println("10");
                break;
            case 9:
                System.out.println("67");
                break;
        }
    }
}
