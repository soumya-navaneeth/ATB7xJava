package July2024.ex_13072024;

public class Lab018_FinalKeyword {
    public static void main(String[] args) {
              /*
        Final keyword in java is used to restrict the user
        Final can be: variable,method OR class
        */
        final double PI=3.14; //defined the value of Pi & cannot be changed
//        PI=5.56; //throws error as "java: cannot assign a value to final variable PI"
        System.out.println(PI); // prints 3.14 instead of 5.56
//        final String STR="TTA";
//        final double D1=999.99;
    }

}
