package July2024.ex_21072024;

public class Lab080_LoopwithCondition {
    public static void main(String[] args) {
        // Loop with condition
        for (int i = 1; i <= 10; i++) {
            // i from 1 to 10, 10 times
            if (i == 5) {
                System.out.println("Five");
            } else if (i== 8) {
                System.out.println("Eight");
            } else {
                System.out.println(i);
            }
        }
    }
}
