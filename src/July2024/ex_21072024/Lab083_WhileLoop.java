package July2024.ex_21072024;

public class Lab083_WhileLoop {
    public static void main(String[] args) {
//  Loops - while and Do-while
//  While: iterate a part of the program repeatedly until the specified Boolean condition is true
/*  Do-while: iterate a part of the program repeatedly, until the specified condition is true,
      but this will execute at least once before checking condition  */

/* While syntax
    while (condition){
    //code to be executed
    Increment / decrement statement
    }    */

        int i = 0; // initialization
        while(i<=10){ //Condition
            System.out.println(i); //Code to be executed
            i++; // Increment/decrement
        }
    }
}
