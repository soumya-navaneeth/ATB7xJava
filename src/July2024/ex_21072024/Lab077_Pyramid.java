package July2024.ex_21072024;

public class Lab077_Pyramid {
    public static void main(String[] args) {
        for(int i=1;i<=7;i++)
        {
            for(int j=1;j<=i;j++)
            {
                System.out.print("* ");
            }
            System.out.println();//new line
        }

    }
}
