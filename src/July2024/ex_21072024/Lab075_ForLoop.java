package July2024.ex_21072024;

public class Lab075_ForLoop {
    public static void main(String[] args) {
/* Loops: to iterate a part of the program several times.
If the number of iteration is fixed, it is recommended to use for loop.
LOOPS Types: For,While,Do-while
For -> number of iteration is fixed, For loop used
While -> number of iteration is not fixed, While Loop is used
Do-While -> number of iteration is not fixed and must have to execute the loop at least once.
Syntax: for(initialization; condition; increment/decrement){....}
 */
        for(int i=1;i <=10;++i) {
            // here is the code you want to execute
            System.out.println("Hi" + i);
        }
    }
}
