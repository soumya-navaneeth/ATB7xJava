package July2024.ex_21072024;

public class Lab086_DoWhileLoop {
    public static void main(String[] args) {
//  Do While: Executes once before validating Condition
//  int -> Code -> condition -> increment / decrement
        int a = 1;
        do {
            System.out.println(a);
            a++;
        }while(a < 10);


    }
}
