package ex_13072024;

public class Lab027_CompundAssignment {
    public static void main(String[] args) {
//  Assignment:used to assign the value on its right to the operand on its left.
//  Compound Assignment Operators: += , -=, /=, *= . %=
        int a=10;
        a+=3; //10+3 = 13
        System.out.println(a);
        a-=4; //13-4 = 9
        System.out.println(a);
        a*=2; //9*2 = 18
        System.out.println(a);
        a/=2; //18/2 = 9
        System.out.println(a);
        a%=2; //9%2 = 1
        System.out.println(a);

    }
}
